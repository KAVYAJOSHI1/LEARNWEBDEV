let grade="A";
switch(grade)
{
    case "A":
    console.log("excellent");
    break;
    case "B":
    console.log("very good");
    break;
    case "C":
    console.log("good");
    break;
    case "D":
    console.log("fair");
    break;
    case "E":
    console.log("poor");
    break;
    default:
    console.log("invalid");

}